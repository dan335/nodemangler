//! Trigonometric operations for the node graph.
//!
//! Provides standard, inverse, and hyperbolic trigonometric functions.

/// Sine function.
pub mod sin;
/// Cosine function.
pub mod cos;
/// Tangent function.
pub mod tan;
/// Inverse sine (arcsine) function.
pub mod asin;
/// Inverse cosine (arccosine) function.
pub mod acos;
/// Inverse tangent (arctangent) function.
pub mod atan;
/// Two-argument inverse tangent (atan2) function.
pub mod atan2;
/// Hyperbolic sine function.
pub mod sinh;
/// Hyperbolic cosine function.
pub mod cosh;
/// Hyperbolic tangent function.
pub mod tanh;
